package T03;

public class Lieferant {

    private String nameVertreter;
    private String firma;
    private String verteilzentrum;
    private String produkte;
    private String alter;
    private String adresse;


    public String getNameVertreter() {
        return nameVertreter;
    }

    public void setNameVertreter(String nameVertreter) {
        this.nameVertreter = nameVertreter;
    }

    public String getFirma() {
        return firma;
    }

    public void setFirma(String firma) {
        this.firma = firma;
    }

    public String getVerteilzentrum() {
        return verteilzentrum;
    }

    public void setVerteilzentrum(String verteilzentrum) {
        this.verteilzentrum = verteilzentrum;
    }

    public String getProdukte() {
        return produkte;
    }

    public void setProdukte(String produkte) {
        this.produkte = produkte;
    }

    public String getAlter() {
        return alter;
    }

    public void setAlter(String alter) {
        this.alter = alter;
    }

    public String getAdresse() {
        return adresse;
    }

    public void setAdresse(String adresse) {
        this.adresse = adresse;
    }
}
