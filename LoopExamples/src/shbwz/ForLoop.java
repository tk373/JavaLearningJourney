package shbwz;

public class ForLoop {
    public static void main(String[] args) {
AufgabeE();
AufgabeB();
}

    public static void AufgabeE(){
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
        public static void AufgabeB() {
            for (int i = 1; i <= 10; i++) {
                System.out.println(i);
            }
        }

}







